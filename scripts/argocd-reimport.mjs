// Re-import ArgoCD Swagger 2.0 → OpenAPI 3.0 (через swagger2openapi)
// → @intent-driven/importer-openapi@0.11+.
//
// Источник: https://raw.githubusercontent.com/argoproj/argo-cd/master/assets/swagger.json
// Скачан в src/domains/argocd/.fixtures/swagger.json (Swagger 2.0).
// Importer читает spec.components.schemas (OpenAPI 3.0 путь), поэтому
// конвертим 2.0 → 3.0 через swagger2openapi перед import'ом.
//
// Usage: node scripts/argocd-reimport.mjs
// Writes: src/domains/argocd/imported.js + .fixtures/openapi3.json (sidecar)
import fs from "node:fs/promises";
import path from "node:path";
import s2o from "swagger2openapi";
import { importOpenApi } from "@intent-driven/importer-openapi";

const SWAGGER_PATH = "src/domains/argocd/.fixtures/swagger.json";
const OPENAPI3_PATH = "src/domains/argocd/.fixtures/openapi3.json";
const OUT_PATH = "src/domains/argocd/imported.js";

const swaggerJson = await fs.readFile(SWAGGER_PATH, "utf-8");
const swagger = JSON.parse(swaggerJson);

console.log(`Source: swagger=${swagger.swagger}, paths=${Object.keys(swagger.paths).length}, definitions=${Object.keys(swagger.definitions).length}`);

// swagger2openapi convertObj options:
// - patch: true — автоматически чинит non-compliant схемы
// - warnOnly: true — не падаем на warning'ах (ArgoCD спека имеет их)
const converted = await s2o.convertObj(swagger, { patch: true, warnOnly: true });
const openapi3 = converted.openapi;
console.log(`Converted to OpenAPI ${openapi3.openapi}: components.schemas=${Object.keys(openapi3.components?.schemas || {}).length}`);

// Sidecar — для debug / diff'а
await fs.writeFile(OPENAPI3_PATH, JSON.stringify(openapi3, null, 2));

const result = importOpenApi(openapi3, { name: "argocd" });

const header = `// Generated by @intent-driven/importer-openapi
// Source: ${SWAGGER_PATH} (Swagger 2.0) → converted to OpenAPI 3.0 via swagger2openapi
// Regenerated: ${new Date().toISOString()}

export const ontology = `;

const body = JSON.stringify(result, null, 2);
const content = header + body + ";\n\n" +
  `export const entities = ontology.entities;\n` +
  `export const intents = ontology.intents;\n` +
  `export const roles = ontology.roles;\n` +
  `export const features = ontology.features || {};\n`;

await fs.writeFile(path.resolve(OUT_PATH), content);
console.log(`\nWrote ${OUT_PATH}:`);
console.log(`  entities: ${Object.keys(result.entities).length}`);
console.log(`  intents:  ${Object.keys(result.intents).length}`);
console.log(`  roles:    ${Object.keys(result.roles).length}`);

// Canonical-entity sanity check — ArgoCD ключевые сущности
const CANONICAL = [
  "Application", "ApplicationSet", "Cluster", "Project",
  "Repository", "GPGKey", "Certificate", "Account",
];
for (const e of CANONICAL) {
  const found = result.entities[e] || result.entities[`v1alpha1${e}`]
    || Object.keys(result.entities).find(k => k.endsWith(e));
  if (found) {
    const entity = typeof found === "string" ? result.entities[found] : found;
    const fields = Object.keys(entity.fields || {});
    const displayName = typeof found === "string" ? found : e;
    console.log(`  ${displayName}.fields (${fields.length}): ${fields.slice(0, 8).join(", ")}${fields.length > 8 ? ", ..." : ""}`);
  } else {
    console.log(`  ${e}: NOT FOUND`);
  }
}
