// Re-import Keycloak Admin REST OpenAPI через @intent-driven/importer-openapi@0.6+
// (flattenSchema для allOf/oneOf composition — G32 / Gravitino #227).
//
// Источник: https://www.keycloak.org/docs-api/latest/rest-api/openapi.yaml
// Скачан в src/domains/keycloak/.fixtures/openapi-bundled.yaml.
//
// Usage: node scripts/keycloak-reimport.mjs
// Writes: src/domains/keycloak/imported.js
import fs from "node:fs/promises";
import path from "node:path";
import { importOpenApi, parseSpec } from "@intent-driven/importer-openapi";

const YAML_PATH = "src/domains/keycloak/.fixtures/openapi-bundled.yaml";
const OUT_PATH = "src/domains/keycloak/imported.js";

const yaml = await fs.readFile(YAML_PATH, "utf-8");
const spec = parseSpec(yaml);
const result = importOpenApi(spec, { name: "keycloak" });

const header = `// Generated by @intent-driven/importer-openapi
// Source: ${YAML_PATH}
// Regenerated: ${new Date().toISOString()}

export const ontology = `;

const body = JSON.stringify(result, null, 2);
const content = header + body + ";\n\n" +
  `export const entities = ontology.entities;\n` +
  `export const intents = ontology.intents;\n` +
  `export const roles = ontology.roles;\n` +
  `export const features = ontology.features || {};\n`;

await fs.writeFile(path.resolve(OUT_PATH), content);
console.log(`Wrote ${OUT_PATH}:`);
console.log(`  entities: ${Object.keys(result.entities).length}`);
console.log(`  intents:  ${Object.keys(result.intents).length}`);
console.log(`  roles:    ${Object.keys(result.roles).length}`);

// Quick canonical-entity sanity checks
for (const e of ["RealmRepresentation", "ClientRepresentation", "UserRepresentation",
                 "GroupRepresentation", "RoleRepresentation",
                 "IdentityProviderRepresentation", "ClientScopeRepresentation",
                 "ComponentRepresentation"]) {
  if (result.entities[e]) {
    const fields = Object.keys(result.entities[e].fields || {});
    console.log(`  ${e}.fields (${fields.length}): ${fields.slice(0, 8).join(", ")}${fields.length > 8 ? ", ..." : ""}`);
  } else {
    console.log(`  ${e}: NOT FOUND`);
  }
}
