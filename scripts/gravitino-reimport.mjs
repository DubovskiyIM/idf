// Re-import Gravitino OpenAPI через @intent-driven/importer-openapi@0.6+
// (flattenSchema для allOf/oneOf composition — G32).
//
// Usage: node scripts/gravitino-reimport.mjs
// Writes: src/domains/gravitino/imported.js
import fs from "node:fs/promises";
import path from "node:path";
import { importOpenApi, parseSpec } from "@intent-driven/importer-openapi";

const YAML_PATH = "src/domains/gravitino/.fixtures/openapi-bundled.yaml";
const OUT_PATH = "src/domains/gravitino/imported.js";

const yaml = await fs.readFile(YAML_PATH, "utf-8");
const spec = parseSpec(yaml);
const result = importOpenApi(spec, { name: "gravitino" });

const header = `// Generated by @intent-driven/importer-openapi (G32: allOf/oneOf merge)
// Source: ${YAML_PATH}
// Regenerated: ${new Date().toISOString()}

export const ontology = `;

const body = JSON.stringify(result, null, 2);
const content = header + body + ";\n\n" +
  `export const entities = ontology.entities;\n` +
  `export const intents = ontology.intents;\n` +
  `export const roles = ontology.roles;\n` +
  `export const features = ontology.features || {};\n`;

await fs.writeFile(path.resolve(OUT_PATH), content);
console.log(`Wrote ${OUT_PATH}:`);
console.log(`  entities: ${Object.keys(result.entities).length}`);
console.log(`  intents:  ${Object.keys(result.intents).length}`);
console.log(`  roles:    ${Object.keys(result.roles).length}`);
// Quick Policy sanity check
if (result.entities.Policy) {
  const fields = Object.keys(result.entities.Policy.fields);
  console.log(`  Policy.fields: ${fields.join(", ")}`);
}
